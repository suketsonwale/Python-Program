print("compute the diameter,circumference and volume of sphere by using class") 
print("----------------------------------------------------------------------") 
r=int(input("Enter radious of sphere :-")) 
print("----------------------------------------------------------------------")  
pi=3.14  
class spare():     
    d=2*r     
    c=2*pi*r     
    v=4*pi*r*r*r/3     

    print("diameter of spare is :-",d)     
    print("circumference of spare :-",c)    
    print("volume of spare is :-",v)  
  
s=spare()